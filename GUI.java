    private void upQuanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upQuanButtonActionPerformed
        String ingrName = (String) ingredientList.getSelectedValue();
        Ingredient selectedIngredient = new Ingredient();
        if(!jToggleButton1.isSelected()){  
            selectedIngredient = theFridge.getIngredient(ingrName);
            if(selectedIngredient != null){
                theFridge.incrementFridgeIngredient(selectedIngredient);
                updateIngredientFields();
            }
        }
        else{
            selectedIngredient.setIngredient(ingrName);
            System.out.println(selectedIngredient.quantity);
            selectedIngredient.addIngredient(1);
            System.out.println(selectedIngredient.quantity);
            updateIngredientFields();
        }
        
    }//GEN-LAST:event_upQuanButtonActionPerformed

    private void downQuanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downQuanButtonActionPerformed
        String ingrName = (String) ingredientList.getSelectedValue();
        Ingredient selectedIngredient = new Ingredient();
        if(!jToggleButton1.isSelected()){ 
        selectedIngredient = theFridge.getIngredient(ingrName);
        if(selectedIngredient != null){
        theFridge.decrease(selectedIngredient);
        if(selectedIngredient.quantity>0){
        updateIngredientFields();      
        }
        else{
            clearFridgeIngredientFields();
            ingredientList.setModel(theFridge.populateFridgeList());
        }
        }// TODO add your handling code here:
        }
        else{
            selectedIngredient.setIngredient(ingrName);
            selectedIngredient.subtractIngredient(1);
            updateIngredientFields();
        }
    }//GEN-LAST:event_downQuanButtonActionPerformed

